static int ed25519_test1(int print)
{
	int i;
	unsigned char skpk[32 + 32], pk[32];
	unsigned char sig[64];

	if ((rom_ed25519_gen_keypair(pk, skpk, keypair_seed) == 0) && (memcmp(pk, skpk + 32, 32) == 0)) {
		if (print) {
			printf("%s gen keypair done\n", __FUNCTION__);
			PrintBuffer("sk", skpk, 32);
			PrintBuffer("pk", pk, 32);
		}
	} else {
		printf("%s gen keypair fail\n", __FUNCTION__);
		return 1;
	}

	for (i = 0; i < 3; i ++) {
		memcpy(skpk, test_data[i].sk, 32);
		memcpy(skpk + 32, test_data[i].pk, 32);

		if (rom_ed25519_gen_signature(sig, test_data[i].m, i, skpk) != 0) {
			printf("%s #%d crypto_sign op error\n", __FUNCTION__, i);
			return 1;
		}

		if (print) {
			PrintBuffer("m", test_data[i].m, i);
			PrintBuffer("exp sig", test_data[i].sig, 64);
			PrintBuffer("sig", sig, 64);
		}

		if (memcmp(sig, test_data[i].sig, 64) == 0) {
			if (print) {
				printf("%s #%d gen sig done\n", __FUNCTION__, i);
			}
		} else {
			printf("%s #%d gen sig fail\n", __FUNCTION__, i);
			return 1;
		}

		if (rom_ed25519_verify_signature(sig, test_data[i].m, i, test_data[i].pk) == 0) {
			if (print) {
				printf("%s #%d verify sig pass\n", __FUNCTION__, i);
			}
		} else {
			printf("%s #%d verify sig fail\n", __FUNCTION__, i);
			return 1;
		}
	}

	return 0;
}

/* ED25519_TEST 2 */
static int ed25519_test2(int print)
{
	unsigned char skpk[64], pk[32], seed[32];
	unsigned char sig[64];
	unsigned char *m = "message to sign";

	if (rom_ed25519_gen_keypair(pk, skpk, seed) == 0) {
		if (print) {
			printf("%s gen keypair done\n", __FUNCTION__);
			PrintBuffer("sk", skpk, 32);
			PrintBuffer("pk", pk, 32);
		}
	} else {
		printf("%s gen keypair fail\n", __FUNCTION__);
		return 1;
	}

	if (rom_ed25519_gen_signature(sig, m, strlen(m), skpk) != 0) {
		printf("%s crypto_sign op error\n", __FUNCTION__);
		return 1;
	}

	if (print) {
		PrintBuffer("sig", sig, 64);
	}

	if (rom_ed25519_verify_signature(sig, m, strlen(m), pk) == 0) {
		if (print) {
			printf("%s verify sig pass\n", __FUNCTION__);
		}
	} else {
		printf("%s verify sig fail\n", __FUNCTION__);
		return 1;
	}

	return 0;
}

int ed25519_test(int print)
{
	int ret;

	if ((ret = ed25519_test1(print)) == 0) {
		ret = ed25519_test2(print);
	}

	printf("%s: %s\n", __FUNCTION__, (ret == 0) ? "PASSED" : "FAILED");
	return ret;
}
