static void PrintBuffer(char *title, unsigned char *buf, int len)
{
	int i;
	printf("\n\r%s:", title);
	for (i = 0; i < len; i ++) {
		if (i % 32 == 0) {
			printf("\n\r");
		}
		printf(" %02x", buf[i]);
	}
	printf("\n\r");
}

